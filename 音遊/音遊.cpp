int main() {
	const char* filePath = "001.wav";
	if (PlaySound(filePath, NULL, SND_FILENAME | SND_ASYNC)) {
    	while(i<=256){	
			system("cls");
			for(int j=i+15;j>=i;j--){//一次顯示6行(高度) 
				for (int x=0;x<4;x++){//掃描 
					if(mus[x][j]==1){//有音符 
						if (kbhit()){//有按到 
							c = getch();	
							if(x==0&&c == 68)	s+=10;//D 
				            if(x==1&&c == 70)	s+=10;//F 
				            if(x==2&&c == 74)	s+=10;//J 
				           	if(x==3&&c == 75)	s+=10;//K 
						} 
						cout<<"0000 ";//顯示音符 
					}	
					else{
						if (kbhit()){//沒打到 
							int c = getch();
							//cout << c;	
							if(c == 68)	s-=3;
				            if(c == 70)	s-=3; 
				            if(c == 74)	s-=3; 
				           	if(c == 75)	s-=3;
						} 
						cout<<"     ";
			        }
				}
				cout<<"\n";
			}
			cout<<"---- ---- ---- ----";//標準線 
			cout<<"\n"<<s;//分數 
			Sleep(100);
			i++;	
		}
    }	
	
}
